#include <stdio.h>

int		ft_str_is_printable(char *str)
{
	if (*str == 0)
		return (1);
	while (*str >= 32 && *str <= 126 && *(str + 1) != 0)
	{
		str++;
	}
	if (*str >= 32 && *str <= 126)
		return (1);
	else
		return (0);
}
