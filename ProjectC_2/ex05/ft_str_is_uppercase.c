#include <stdio.h>

int		ft_str_is_uppercase(char *str)
{
	int		x;
	int		a;

	a = 0;
	x = 1;
	while (*(str + a) != 0 && x == 1)
	{
		if (*(str + a) > 64 && *(str + a) < 91)
		{
			x = 1;
		}
		else
		{
			x = 0;
		}
		a++;
	}
	return (x);
}
